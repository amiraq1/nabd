"""Simple ANSI color definitions for terminal UI."""

class Colors:
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKCYAN = '\033[96m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'

def colorize(text: str, color: str) -> str:
    """Wrap text in an ANSI color code, and reset it after."""
    if not color:
        return text
    return f"{color}{text}{Colors.ENDC}"
